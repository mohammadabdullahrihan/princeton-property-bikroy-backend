// Flatten for search/autocomplete
const getAllDivisions = () => Object.keys(bangladeshLocations)

const getDistrictsByDivision = (division) => {
  return bangladeshLocations[division] ? Object.keys(bangladeshLocations[division]) : []
}

const getAreasByDistrict = (division, district) => {
  return bangladeshLocations[division]?.[district] || []
}

const searchLocations = (query) => {
  const results = []
  const lowerQuery = query.toLowerCase()
  
  Object.entries(bangladeshLocations).forEach(([division, districts]) => {
    if (division.toLowerCase().includes(lowerQuery)) {
      results.push({ type: 'division', name: division })
    }
    
    Object.entries(districts).forEach(([district, areas]) => {
      if (district.toLowerCase().includes(lowerQuery)) {
        results.push({ type: 'district', name: district, division })
      }
      
      areas.forEach(area => {
        if (area.toLowerCase().includes(lowerQuery)) {
          results.push({ type: 'area', name: area, district, division })
        }
      })
    })
  })
  
  return results.slice(0, 10) // Limit to 10 results
}

module.exports = {
  bangladeshLocations,
  getAllDivisions,
  getDistrictsByDivision,
  getAreasByDistrict,
  searchLocations,
}
